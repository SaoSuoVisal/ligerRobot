#pragma config(Motor,  port2,           FR,            tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port3,           FL,            tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           BR,            tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           BL,            tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port7,           lift,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port8,           sisL,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port9,           sisR,          tmotorVex393_MC29, openLoop, reversed)
//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//

int threshold = 15; // torelance between each joystick
int limitRPM = 160; // For high torque motor the has a limit speed
int sisSpeed = 127; // For scissors speed
task main()
{
	while(true){
		int y = vexRT[Ch2];
		int x = vexRT[Ch1];
		int r = vexRT[Ch4];


   if(abs(y) > threshold && abs(y) < limitRPM)	y = vexRT[Ch2];
   else	y = 0;

   if(abs(x) > threshold && abs(x) < limitRPM)	x = vexRT[Ch1];
   else	x = 0;

   if(abs(r) > threshold)	r = vexRT[Ch4]/2;
   else	r = 0;

		// Y component, X component, Rotation
		motor[FL] =   y + x + r;
		motor[FR] =  -y + x + r;
		motor[BR] =  -y - x + r;
		motor[BL] =   y - x + r;

		// Arm Control
    if(vexRT[Btn6U] == 1){
      motor[lift] = 70;
    } else if(vexRT[Btn6D] == 1){
      motor[lift] = -70;
    } else{
      motor[lift] = 0;
    }

    // Scissors Lift
    if(vexRT[Btn5U] == 1){
      motor[sisL] = sisSpeed;
      motor[sisR] = sisSpeed;
    } else if(vexRT[Btn5D] == 1){
      motor[sisL] = -sisSpeed;
      motor[sisR] = -sisSpeed;
    } else{
      motor[sisL] = 0;
      motor[sisR] = 0;
    }

    // Motor values can only be updated every 20ms
		wait10Msec(2);
	}

}
